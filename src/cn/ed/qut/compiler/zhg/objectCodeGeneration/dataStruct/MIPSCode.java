package cn.ed.qut.compiler.zhg.objectCodeGeneration.dataStruct;

public class MIPSCode {
	private String OP;
	private String arg1;
	private String arg2;
	private String res;
	
	
	
	public String getOP() {
		return OP;
	}
	public void setOP(String oP) {
		OP = oP;
	}
	public String getArg1() {
		return arg1;
	}
	public void setArg1(String arg1) {
		this.arg1 = arg1;
	}
	public String getArg2() {
		return arg2;
	}
	public void setArg2(String arg2) {
		this.arg2 = arg2;
	}
	public String getRes() {
		return res;
	}
	public void setRes(String res) {
		this.res = res;
	}
	
	
}
